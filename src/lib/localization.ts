function translateListFragment(value: string, locale: Locale): string {
  return value
    .split(",")
    .map((part) => part.trim())
    .filter(Boolean)
    .map((part) => translateText(part, locale))
    .join(", ");
}




function translateSourceGeneratedText(value: string, locale: Locale): string | null {
  if (locale === "en") {
    return null;
  }

  const httpMatch = /^HTTP (\d{3}) from (.+)$/.exec(value);
  if (httpMatch) {
    return "HTTP " + httpMatch[1] + " от " + httpMatch[2];
  }

  return null;
}

function translateApplicationTipGeneratedText(value: string, locale: Locale): string | null {
  if (locale === "en") {
    return null;
  }

  const requiredDocumentsMatch = /^Required documents: (.+)$/.exec(value);
  if (requiredDocumentsMatch) {
    return "Необходими документи: " + translateListFragment(requiredDocumentsMatch[1], locale);
  }

  const evaluationCriteriaMatch = /^Evaluation criteria: (.+)$/.exec(value);
  if (evaluationCriteriaMatch) {
    return "Критерии за оценка: " + translateListFragment(evaluationCriteriaMatch[1], locale);
  }

  const applicationFocusMatch = /^Application focus: (.+)$/.exec(value);
  if (applicationFocusMatch) {
    return "Фокус на кандидатурата: " + translateListFragment(applicationFocusMatch[1], locale);
  }

  const whoCanApplyMatch = /^Who can apply: (.+)$/.exec(value);
  if (whoCanApplyMatch) {
    return "Кой може да кандидатства: " + translateListFragment(whoCanApplyMatch[1], locale);
  }

  return null;
}

function translateEligibilityGeneratedText(value: string, locale: Locale): string | null {
  if (locale === "en") {
    return null;
  }

  const sectorFitMatch = /^(.+) is listed as a strong sector fit\.$/.exec(value);
  if (sectorFitMatch) {
    return "Секторът \"" + translateText(sectorFitMatch[1], locale) + "\" е посочен като силно съответствие.";
  }

  const stageFitMatch = /^The (.+) stage fits the programme profile\.$/.exec(value);
  if (stageFitMatch) {
    return "Етапът \"" + translateText(stageFitMatch[1], locale) + "\" съответства на профила на програмата.";
  }

  const alignedStagesMatch = /^The programme is better aligned with (.+) stage companies\.$/.exec(value);
  if (alignedStagesMatch) {
    return "Програмата е по-подходяща за компании на етап " + translateListFragment(alignedStagesMatch[1], locale) + ".";
  }

  const mustAddressMatch = /^This section must clearly address: (.+)\.$/.exec(value);
  if (mustAddressMatch) {
    return "Този раздел трябва ясно да адресира: " + translateText(mustAddressMatch[1], locale) + ".";
  }

  const makeSureAddressMatch = /^Make sure this section clearly addresses: (.+)\.$/.exec(value);
  if (makeSureAddressMatch) {
    return "Уверете се, че този раздел ясно адресира: " + translateText(makeSureAddressMatch[1], locale) + ".";
  }

  return null;
}

function translateDocumentTemplateText(value: string, locale: Locale): string | null {
  if (locale === "en") {
    return null;
  }

  const coreProposalMatch = /^Core proposal structure for (.+)\.$/.exec(value);
  if (coreProposalMatch) {
    return `Основна структура на проектното предложение за ${translateText(coreProposalMatch[1], locale)}.`;
  }

  const summaryMatch = /^Summarize the startup, project objective, target customer, requested support, and why this programme is a fit for a (.+) company\.$/.exec(value);
  if (summaryMatch) {
    const rawStage = summaryMatch[1];
    const translatedStage = rawStage === "R&D" ? "НИРД" : translateText(rawStage, locale);
    const stageLabel = /^[A-Z&]+$/.test(translatedStage) ? translatedStage : translatedStage.toLowerCase();
    const companyDescription = rawStage === "technology" ? "технологична компания" : `компания на етап ${stageLabel}`;
    return `Обобщете стартъпа, целта на проекта, целевия клиент, исканата подкрепа и защо тази програма е подходяща за ${companyDescription}.`;
  }

  const focusMatch = /^Reference the programme's focus on (.+)\.$/.exec(value);
  if (focusMatch) {
    return `Посочете фокуса на програмата върху ${translateListFragment(focusMatch[1], locale)}.`;
  }

  const eligibilityMatch = /^Use the official eligibility framing from (.+)\.$/.exec(value);
  if (eligibilityMatch) {
    return `Използвайте официалната рамка за допустимост от ${translateText(eligibilityMatch[1], locale)}.`;
  }

  return null;
}

function escapeRegExp(value: string): string {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function hasUntranslatedProjectText(value: string): boolean {
  const withoutAllowedTerms = value
    .replace(/\b(EU|EIC|EIT|STEP|PCIE|RIDST|ISUN|EUMIS|EUIPO|AI|IP|ICT|R&D|SaaS|MVP|ERP|CRM|STEM|IS3|BGN|EUR|IIoT|AR|VR|ML|TRL|DFZ|SEU|OGF|FISP|BAS|EC|VAT)\b/g, "")
    .replace(/[A-Z]{2,}[0-9A-Z.-]*/g, "")
    .replace(/[0-9\s.,:;/%()[\]{}&+\-/'"–—]/g, "");

  return /[A-Za-z]{3,}/.test(withoutAllowedTerms);
}

function normalizeTranslatedProjectText(value: string): string {
  return value
    .replace(/\s+/g, " ")
    .replace(/\s+([,.;:])/g, "$1")
    .replace(/\(\s+/g, "(")
    .replace(/\s+\)/g, ")")
    .trim();
}

function translateProgramPhrase(value: string): string | null {
  const wordCount = value.trim().split(/\s+/).filter(Boolean).length;
  if (wordCount > 24 || /[.!?;]/.test(value)) {
    return null;
  }

  let translated = value;
  const sorted = [...programPhraseTranslations].sort((a, b) => b[0].length - a[0].length);
  for (const [source, target] of sorted) {
    translated = translated.replace(new RegExp(`\\b${escapeRegExp(source)}\\b`, "g"), target);
  }

  translated = normalizeTranslatedProjectText(translated);
  if (translated === value || hasUntranslatedProjectText(translated)) {
    return null;
  }

  return translated;
}

function hasMixedUntranslatedLatin(value: string): boolean {
  if (!/[\u0400-\u04ff]/.test(value)) {
    return false;
  }

  const withoutAllowedAcronyms = value
    .replace(/\b(EU|EIC|EIT|STEP|PCIE|RIDST|ISUN|EUMIS|EUIPO|AI|IP|ICT|R&D|SaaS|MVP|ERP|CRM|STEM|IS3|BGN|EUR|IIoT|AR|VR|ML)\b/g, "")
    .replace(/[0-9\s.,:;/%()&+\-\/]/g, "");

  return /[A-Za-z]/.test(withoutAllowedAcronyms);
}

function shouldUsePhraseFallback(value: string): boolean {
  const wordCount = value.trim().split(/\s+/).filter(Boolean).length;
  return wordCount <= 8 && !/[.!?;]/.test(value);
}

export function translateText(value: string, locale: Locale): string {
  if (locale === "en" || !value) {
    return value;
  }

  const exact = remainingProgramTextTranslations[value] ?? sourceTextTranslations[value] ?? applicationTipTextTranslations[value] ?? eligibilityTextTranslations[value] ?? documentTemplateTextTranslations[value] ?? projectDetailTextTranslations[value] ?? projectTextTranslations[value] ?? exactTextTranslations[value] ?? valueLabels.bg[value];
  if (exact) {
    return exact;
  }

  const sourceGenerated = translateSourceGeneratedText(value, locale);
  if (sourceGenerated) {
    return sourceGenerated;
  }

  const applicationTipGenerated = translateApplicationTipGeneratedText(value, locale);
  if (applicationTipGenerated) {
    return applicationTipGenerated;
  }

  const eligibilityGenerated = translateEligibilityGeneratedText(value, locale);
  if (eligibilityGenerated) {
    return eligibilityGenerated;
  }

  const documentTemplate = translateDocumentTemplateText(value, locale);
  if (documentTemplate) {
    return documentTemplate;
  }

  const programPhrase = translateProgramPhrase(value);
  if (programPhrase) {
    return programPhrase;
  }

  if (!shouldUsePhraseFallback(value)) {
    return value;
  }

  let translated = value;
  for (const [source, target] of sortedPhraseTranslations) {
    translated = translated.split(source).join(target);
  }

  return hasMixedUntranslatedLatin(translated) ? value : translated;
}

export function translateTexts(values: string[], locale: Locale): string[] {
  return values.map((value) => translateText(value, locale));
}
